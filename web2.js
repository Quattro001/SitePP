document.addEventListener('DOMContentLoaded', () => {
    const container = document.querySelector('.course-content');
    
    content.forEach((chapter, chapterIndex) => {
        const card = document.createElement('div');
        card.className = 'chapter';
        
        const title = document.createElement('div');
        title.className = 'chapter-title';
        title.textContent = chapter.title;
        
        const subList = document.createElement('ul');
        subList.className = 'subitems';
        
        chapter.items.forEach((item, itemIndex) => {
            const li = document.createElement('li');
            li.textContent = item;
            li.style.cursor = 'pointer';
            
            li.addEventListener('click', (e) => {
                e.stopPropagation();
                const chapterNumber = chapterIndex + 1;
                const itemNumber = itemIndex + 1;
                window.location.href = `chapter-${chapterNumber}-item-${itemNumber}.html`;
            });
            
            subList.appendChild(li);
        });

        title.addEventListener('click', (e) => {
            e.stopPropagation();
            subList.classList.toggle('show');
        });

        card.addEventListener('click', () => {
            card.classList.toggle('active');
        });

        card.appendChild(title);
        card.appendChild(subList);
        container.appendChild(card);
    });
});